fun main() {
    var firstname = "Mervyn"
    var lastname = "Kamau"
    var school = "eMobilis"

    //String Concatenation
    println(firstname + lastname)
    println(firstname + " " + lastname)

    println(firstname[0])

    println(firstname.uppercase())
    println(school.lowercase())

    //String Interpolation
    println("My fullname is $firstname $lastname. I study at $school")


}