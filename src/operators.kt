fun main() {
    //Arithmetic Operators - Simple calculations
    var x = 100
    var y = 10

    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y) //Modulus - Returns remainder


    //Comparison Operators - Returns boolean
    var a = 89
    var b = 56

    println(a > b)
    println(a < b)
    println(a >= b)
    println(a <= b)
    println(a == b)
    println(a != b)


    //Assignment Operators - Assign values to variables
    var z = 45
    println(z)

    z += 5
    println(z)


    //Logical Operators - &&, ||, !
    var log = 78

    println(log > 34 && log < 60)
    println(log > 34 || log < 60)



}